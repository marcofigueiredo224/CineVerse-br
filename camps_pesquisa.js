
function clickPesquisar() {
    if (campo_pesquisa.style.display == "block") {
        campo_pesquisa.style.display = "none";
    } else{
        campo_pesquisa.style.display = "block";
    }
}


function clickMenu() {
    if (menu.style.display == "block") {
        menu.style.display = "none";
    } else{
        menu.style.display = "block";
    }
}